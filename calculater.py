num1=int(input("enter a first number"))
num2=int(input("enter a second number"))
symbol=input("enter a simbols")
if symbol=="+":
    print(num1+num2)
elif symbol=="-":
    print(num1-num2)
elif symbol=="*":
    print(num1*num2)
elif symbol=="%":
    print(num1%num2)
elif symbol=="/":
    print(num1/num2)
else:
    print("invalid")




